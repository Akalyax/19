void	ft_rev_int_tab(int *tab, int size)
{
	int	t;
	int	wait;

	t = 1;
	while (t <= (size / 2))
	{
		wait = tab[size - t];
		tab[size - t] = tab[t - 1];
		tab[t - 1] = wait;
		t++;
	}
}
