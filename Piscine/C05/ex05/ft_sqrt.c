int	ft_sqrt(int nb)
{
	int	i;
	int	pow;

	pow = 0;
	i = 0;
	while (pow <= nb)
	{
		i++;
		pow = i * i;
	}
	i--;
	if ((i * i) == nb)
		return (i);
	return (0);
}
