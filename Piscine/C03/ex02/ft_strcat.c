char	*ft_strcat(char *dest, char *src)
{
	int	q;
	int	l;

	q = 0;
	l = 0;
	while (dest[l] != 0)
	{
		l++;
	}
	while (src[q] != 0)
	{
		dest[l + q] = src[q];
		q++;
	}
	dest[l + q] = '\0';
	return (dest);
}
