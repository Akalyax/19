#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	x;

	x = 0;
	if (n <= 0)
		return (0);
	while (*s1 && *s1 == *s2 && x < n - 1)
		x++;
	return (*s1 - *s2);
}
